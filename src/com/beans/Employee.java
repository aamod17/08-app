package com.beans;

public class Employee {
	
	public String name;
	public int rollNo;
	public String city;
	public double salary;
	
	public Employee(String name, int rollNo, String city, double salary ) {
		this.name = name;
		this.rollNo = rollNo;
		this.city = city;
		this.salary = salary;
	}
	
	
}
